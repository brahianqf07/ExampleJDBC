    public void fillTable()
    {
        try {
            DefaultTableModel model = new DefaultTableModel();
            jTableEmployee_Type.setModel(model);
            model.addColumn("Id");
            model.addColumn("Descripcion");
            
            String [] rows  = new String[2];
             EmployeeTypeControllers controllers = new EmployeeTypeControllers();
             List<EmployeeType> types = controllers.findALL();
            for (EmployeeType type : types)
            {
                rows[0] = String.valueOf(type.getId());
                rows[1] = type.getDescript();
                model.addRow(rows);
            }
        } catch (Exception e) {
            MessageUtils.showErrorMessage(e.getMessage());
        }
    }

